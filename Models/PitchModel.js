import mongoose from "mongoose";

const PitchSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    location: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Pitch = mongoose.model("Pitch", PitchSchema);

export default Pitch;
